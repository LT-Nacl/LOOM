void handle_input(struct player *p, float move_speed, float turn_speed, long long millis) {
	int ch = getchar();
	if (ch == -1 || ch == EOF) printf("return");

	//printf("Key: %c, pos: (%.2f, %.2f), angle: %.2f\n", ch, p->x, p->y, p->state);

	switch(ch) {
		case 'w':
			if(!test_arr[(int)(p->x +cosf(p->state) * move_speed)][(int)(p->y + sinf(p->state) * move_speed)]){
				p->x += cosf(p->state) * move_speed;
				p->y += sinf(p->state) * move_speed;
			}
			break;
		case 's':
			if(!test_arr[(int)(p->x -cosf(p->state) * move_speed)][(int)(p->y - sinf(p->state) * move_speed)]){
				p->x -= cosf(p->state) * move_speed;
				p->y -= sinf(p->state) * move_speed;
			}
			break;
		case 'a':
			p->state -= turn_speed;
			break;
		case 'd': 
			p->state += turn_speed;
			break;
		case ' ':
			if(millis-last > 500 ){
				shooting = 1;
				last = millis;
				struct hit * bullet = cast_rays(p->state, p->x, p->y, test_arr, pellets,HALF_FOV);
				for(int i = 0; i < pellets; i++){
					if(bullet[i].value == 'E'){
						enemyList[0].hp = 0;
						test_arr[bullet[i].map_y][bullet[i].map_x] = '@';
						score += 10;
						last_enemy = 0;
					}
				}
			}

			break;
		case 'q': 
			raw_end(); 
			exit(0);

	}

	p->state = fmodf(p->state, 2 * M_PI);
}



void test_world_and_rays()
{

	enemyList[0].hp = 100;
	enemyList[0].x = 2.0f;
	enemyList[0].y = 2.0f;
	enemyList[0].dmg = 10;
	enemyList[0].face = 'E';

	int s_x = 64;
	int s_y = 64;
	struct timespec ts;


	for (int x = 0; x < s_x; x++) {
		for (int y = 0; y < s_y; y++) {
			test_arr[y][x] = edit_world[y][x];
		}
	}

	struct player p;
	p.x = 32;
	p.y = 32;
	p.state = 0.0f;

	//float offset = 0.01f;
	p.state = 0;
	struct frame f;
	f.id = 0;
	f.width = 80;
	f.height = 45;
	clear_frame(&f);
	struct frame * test_f = &f;
	raw_begin();
	while(1){
		timespec_get(&ts, TIME_UTC);
		long long millis = (long long)ts.tv_sec * 1000 + ts.tv_nsec / 1000000;
		/* STATIC ANIMATION
		   p.state += offset;
		   p.state = fmod(p.state, 2 * M_PI);
		   */
		//enemys
		for(int i = 0; i < 1; i++){
			if(enemyList[i].hp >= 1){
				int old_x = (int)enemyList[i].x;
				int old_y = (int)enemyList[i].y;
				test_arr[old_y][old_x] = 0; 

				double angle_to_play = atan2(p.y - enemyList[i].y, p.x - enemyList[i].x);
				enemyList[i].x += cosf(angle_to_play) * 0.10f;
				enemyList[i].y += sinf(angle_to_play) * 0.10f;

				int x = (int)enemyList[i].x;
				int y = (int)enemyList[i].y;

				if(x == (int)p.x && y == (int)p.y){
					hp -= enemyList[i].dmg;
					enemyList[i].hp = 0;
					test_arr[y][x] = 0;
				} else {
					test_arr[y][x] = enemyList[i].face; 
				}
			}
		}
		if (enemyList[0].hp <= 0 && (millis - last_enemy > 6000)) {
			int valid_spot = 0;
			int rx, ry;

			while (!valid_spot) {
				rx = rand() % 64;
				ry = rand() % 64;

				if (test_arr[ry][rx] == 0 && (int)p.x != rx && (int)p.y != ry) {
					valid_spot = 1;
				}
			}


			enemyList[0].x = (float)rx + 0.5f;
			enemyList[0].y = (float)ry + 0.5f;
			enemyList[0].hp = 100;
			enemyList[0].face = 'E';


			test_arr[ry][rx] = 'E';

			printf("Enemy Respawned at %d, %d\n", rx, ry);
		}


		int num_rays = 128;
		struct hit * scan;
		scan = p_cast_rays(&p, test_arr, num_rays);
		for (int i = 0; i < num_rays; i++) {
			float dy =  (f.height / 2.0f) * x_max / fmaxf(scan[i].distance, 0.1f);
			int low = f.height/2 - (int)dy;
			int high = f.height/2 + (int)dy;
			int x = (i * f.width) / num_rays;
			if (low < 0) low = 0;
			if (high >= f.height) high = f.height - 1;
			if (x >= 0 && x < f.width && low <= high) {
				draw_col_sing(test_f, (scan[i].value == 2) ? '#' : (scan[i].value == 'E' ? 'E' : '@'), x, low, high);
			}
		}

		//text
		char buffer[16][64];


		sprintf(buffer[0], "X: %lf Y: %lf HP %d SCORE %d", p.x, p.y, hp, score);
		draw_text(test_f, buffer[0], 0, 44, 0xffffff);


		// time



		// sprites
		draw_sprite(test_f, (const char*)shotgun, 12, 8, 0, f.height - 8);
		draw_sprite(test_f, (const char*)crosshair, 5, 5, f.width/2 - 2, f.height/2 - 2);
		if(shooting){
			draw_sprite(test_f, (const char*)blast, 8, 8, 15, f.height - 20);
			if(millis - last > 300){
				shooting = 0;
			}
		}
		if (hp <= 0) {
			clear_frame(test_f);
			draw_text(test_f, "=== GAME OVER ===", f.width/2 - 8, f.height/2 - 2, 0xFF0000);
			draw_text(test_f, "You died.", f.width/2 - 4, f.height/2, 0xFFFFFF);
			draw_text(test_f, "Press Q to Quit", f.width/2 - 7, f.height/2 + 2, 0xFFFFFF);
			frame_draw(test_f);


			while(1) {
				int ch = getchar();
				if(ch == 'q') {
					raw_end();
					exit(0);
				}
			}
		}
		frame_draw(test_f);
		free(scan);
		handle_input(&p, 1.0f, .1f,millis);
		usleep((int)(1000000 * (1.0f/FRAMERATE)));
	}
	free(test_f);

	usleep(1*1000000);


}


